def Render(colourName):
    if colourName == "red":
        return (0,0,255)
    elif colourName == "green":
        return (0,255,0)
    elif colourName == "blue":
        return (255,0,0)
    elif colourName == "cyan":
        return (255,255,0)
    elif colourName == "yellow":
        return (0,255,255)
    elif colourName == "purple":
        return (255,0,255)
    elif colourName == "white":
        return (255,255,255)
    elif colourName == "black":
        return (0,0,0)
